function M.setup()
	-- Create custom dashboard
	local alpha_ok, alpha = pcall(require, "alpha")
	if not alpha_ok then
		vim.notify("Alpha not installed. Run :Lazy sync", vim.log.levels.WARN)
		return
	end

	local dashboard = require("alpha.themes.dashboard")

	-- Seed random number generator
	math.randomseed(os.time())

	-- Pick random logo
	local random_logo = logos[math.random(#logos)]
	local logo_art = random_logo.art
	local logo_title = random_logo.title

	-- Format title with borders
	local formatted_title = string.format(
		[[
    ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    ‚ïë  %s
    ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
]],
		logo_title
	)

	-- Set header with random logo
	dashboard.section.header.val = vim.split(logo_art .. "\n" .. formatted_title, "\n")

	-- Random quote
	local quote = quotes[math.random(#quotes)]
	dashboard.section.footer.val = "üéÆ " .. quote

	-- Set menu
	dashboard.section.buttons.val = {
		dashboard.button("f", "  Find file", ":Telescope find_files <CR>"),
		dashboard.button("e", "  New file", ":ene <BAR> startinsert <CR>"),
		dashboard.button("r", "  Recently used files", ":Telescope oldfiles <CR>"),
		dashboard.button("t", "  Find text", ":Telescope live_grep <CR>"),
		dashboard.button("c", "  Configuration", ":e ~/.config/nvim/init.lua <CR>"),
		dashboard.button("q", "  Quit Neovim", ":qa<CR>"),
	}

	-- Set highlight colors
	dashboard.section.header.opts.hl = "Type"
	dashboard.section.buttons.opts.hl = "Keyword"
	dashboard.section.footer.opts.hl = "Constant"

	-- Send config to alpha
	alpha.setup(dashboard.opts)

	-- Disable folding on alpha buffer
	vim.cmd([[
    autocmd FileType alpha setlocal nofoldenable
  ]])
end

return M
